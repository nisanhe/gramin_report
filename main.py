# --- הגדרות ---
NEW_CSV_PATH = '/content/drive/MyDrive/Garmin_Project/garmin_master_2026_rebuilt.csv'
START_DATE = "2026-01-01"

def rebuild_2026_dataset():
    try:
        # 1. התחברות לגרמין
        print("🔗 Connecting to Garmin...")
        client = Garmin(os.environ.get('garmin_email'), os.environ.get('garmin_pass'))
        client.login()

        # 2. שליפת כל הפעילויות מ-2026
        print(f"⏳ Fetching all activities since {START_DATE}...")
        activities = client.get_activities_by_date(START_DATE, datetime.now().date().isoformat())
        print(f"✅ Found {len(activities)} activities.")

        all_rows = []

        for act in activities:
            # חילוץ נתונים בסיסי
            dist_m = act.get('distance', 0)
            duration_sec = act.get('duration', 0)
            dist_km = round(dist_m / 1000, 2)
            duration_min = round(duration_sec / 60, 2)

            # חישוב אורך צעד (ס"מ)
            stride_cm = act.get('avgStrideLength', 0)
            cadence = act.get('avgCadence', 0)
            if (stride_cm == 0 or stride_cm is None) and cadence > 0 and duration_min > 0:
                total_steps = cadence * duration_min
                stride_cm = (dist_km * 100000) / total_steps if total_steps > 0 else 0

            # בניית השורה עם כל המדדים שביקשת
            row = {
                'activity_id': act.get('activityId'),
                'date': act.get('startTimeLocal'),
                'type': act.get('activityType', {}).get('typeKey'),
                'distance_km': dist_km,
                'duration_min': duration_min,
                'pace_min_km': round(duration_min / dist_km, 2) if dist_km > 0 else 0,
                'avg_hr': act.get('averageHR', 0),
                'max_hr': act.get('maxHR', 0), # השדה שתיקנו
                'cadence': round(cadence, 1),
                'stride_length_cm': round(stride_cm, 1),
                'vertical_oscillation': act.get('avgVerticalOscillation', 0),
                'ground_contact_time': act.get('avgGroundContactTime', 0),
                'elevation_gain': act.get('elevationGain', 0),
                'elevation_loss': act.get('elevationLoss', 0),
                'aerobic_te': act.get('aerobicTrainingEffect', 0),
                'anaerobic_te': act.get('anaerobicTrainingEffect', 0),
                'vo2_max': act.get('vO2MaxValue', 0),
                'calories': act.get('calories', 0)
            }
            all_rows.append(row)

        # 3. יצירת DataFrame וסידור לפי תאריך
        df = pd.DataFrame(all_rows)
        df['date'] = pd.to_datetime(df['date'])
        df = df.sort_values('date').reset_index(drop=True)

        # 4. חישוב מדדי עומס (Workload & ACWR)
        print("📈 Calculating training metrics...")
        df['workload'] = df['distance_km'] * (df['avg_hr'] / 100)
        df['acute'] = df['workload'].rolling(window=7, min_periods=1).mean()
        df['chronic'] = df['workload'].rolling(window=28, min_periods=1).mean()
        df['acwr'] = (df['acute'] / df['chronic']).replace([np.inf, -np.inf], 0).fillna(0)

        # 5. שמירה לקובץ חדש
        df.to_csv(NEW_CSV_PATH, index=False)
        print(f"🏁 DONE! New database saved to: {NEW_CSV_PATH}")

        return df

    except Exception as e:
        print(f"❌ Error during rebuild: {e}")

# הרצה
