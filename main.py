"""# Summerize Data weekly comparison"""

def get_weekly_comparison_v2(df):
    today = datetime.now().date()
    periods = [
        (today - timedelta(days=6), today, "This Week"),
        (today - timedelta(days=13), today - timedelta(days=7), "Last Week"),
        (today - timedelta(days=20), today - timedelta(days=14), "2 Weeks Ago")
    ]

    comparison_data = []
    for start, end, label in periods:
        mask = (df['date'].dt.date >= start) & (df['date'].dt.date <= end)
        week_df = df.loc[mask]

        if not week_df.empty:
            # חישוב המדדים לשבוע פעיל
            stats = {
                'label': label,
                'num_runs': len(week_df), # מספר ריצות
                'distance': week_df['distance_km'].sum(),
                'longest_run': week_df['distance_km'].max(), # הריצה הכי ארוכה
                'total_climb': week_df['elevation_gain'].sum(), # טיפוס מצטבר
                'avg_pace': week_df['pace_min_km'].mean(),
                'avg_hr': week_df['avg_hr'].mean(),
                'stride': week_df['stride_length_cm'].mean(),
                'acwr': week_df['acwr'].iloc[-1],
                'efficiency': (week_df['distance_km'].sum() / (week_df['avg_hr'].mean() * (week_df['duration_min'].sum()/60))) if week_df['avg_hr'].mean() > 0 else 0
            }
        else:
            stats = {k: 0 for k in [
                'num_runs', 'distance', 'longest_run', 'total_climb',
                'avg_pace', 'avg_hr', 'stride', 'acwr', 'efficiency'
            ]}
            stats['label'] = label

        comparison_data.append(stats)

    return comparison_data

